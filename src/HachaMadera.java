public class HachaMadera {

    private int durabilidad;
    private int fuerza;

    public HachaMadera() {
        this.durabilidad = 100;
        this.fuerza = 2;
    }

    public int getDurabilidad() {
        return durabilidad;
    }

    public int getFuerza() {
        return fuerza;
    }
}