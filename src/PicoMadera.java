public class PicoMadera {
    private int durabilidad;
    private int fuerza;


    public PicoMadera() {
        this.durabilidad = 100;
        this.fuerza = 2;
    }

    public int getDurabilidad() {
        return durabilidad;
    }

    public int getFuerza() {
        return fuerza;
    }
}
